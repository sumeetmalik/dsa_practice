import java.util.*;

public class variable_basics {
    public static void main(String[] args){
        int i = 10;
        String str = "hello";
        double db = 4.5; // float
        char ch = 'a';

        System.out.println("i = " + i);
        System.out.println("str = " + str);
        System.out.println("db = " + db);
        System.out.println("ch = " + ch);
    }
}